fn main() {
    let uinteger: u8 = 5;
    let integer: i8 = -5;
    let character: char = 'x';
    let float: f32 = 5.5;
    println!("This is Unsigned Integer {}", uinteger);
    println!("This is Signed Integer {}", integer);
    println!("This is Character {}", character);
    println!("This is Floating Point Number {}", float);
}
